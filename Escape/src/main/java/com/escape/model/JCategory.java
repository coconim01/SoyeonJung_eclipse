package com.escape.model;

public class JCategory {
	private String jcname;
	
	public JCategory() {
	}

	@Override
	public String toString() {
		return "JCategory [jcname=" + jcname + "]";
	}

	public String getJcname() {
		return jcname;
	}

	public void setJcname(String jcname) {
		this.jcname = jcname;
	}
	
	
}
